// constant pattern 
void main(){
  var number = 5;
  switch(number){
    case 5:
        print(number);
        break;
  }

  // another example
  var str = null;
  switch(str){
    case null:
        print(null);
        break;
  }

  // another example
  var name = 'google';
  switch(name){
    case 'google':
        print('google');
        break;
  }
}