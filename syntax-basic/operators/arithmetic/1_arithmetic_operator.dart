// arithmetic operator
void main(){
    double result1 = 7 / 3;   // division operator
    int result2 = 7 ~/ 3;    // integer division operator
    print(result1);   // 7.33333
    print(result2);   //7

    int number = 5;
    int result3 = -number;   // unary minus operator return the absalute value
    print(result3);
}
